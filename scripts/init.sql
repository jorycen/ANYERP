  PAYMENT_METHOD VARCHAR(64) NOT NULL COMMENT '支付方式',
  AMOUNT       DECIMAL(12,2) NOT NULL COMMENT '支付金额',
  PAYMENT_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
