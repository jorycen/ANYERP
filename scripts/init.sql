-- ==========================================
-- ANY-ERP 数据库初始化脚本 v1.0
-- ==========================================

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

-- -------------------------------------------
-- 一、组织架构
-- -------------------------------------------

-- T_REGION: 区域表
DROP TABLE IF EXISTS T_REGION;
CREATE TABLE T_REGION (
  REGION_ID     VARCHAR(32) PRIMARY KEY COMMENT '区域ID',
  REGION_CODE   VARCHAR(32) NOT NULL UNIQUE COMMENT '区域代码(CD/CQ/DS)',
  NAME          VARCHAR(128) NOT NULL COMMENT '区域名称',
  SORT_ORDER    INT DEFAULT 0 COMMENT '排序',
  STATUS        TINYINT DEFAULT 1 COMMENT '状态：1-启用，0-停用',
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='区域表';

-- T_DISTRIBUTOR: 经销商/股东表
DROP TABLE IF EXISTS T_DISTRIBUTOR;
CREATE TABLE T_DISTRIBUTOR (
  DISTRIBUTOR_ID VARCHAR(32) PRIMARY KEY COMMENT '经销商ID',
  REGION_ID      VARCHAR(32) COMMENT '所属区域',
  NAME           VARCHAR(255) NOT NULL COMMENT '名称',
  EQUITY_RATIO  DECIMAL(5,2) COMMENT '股权比例(%)',
  PHONE         VARCHAR(32) COMMENT '联系电话',
  ADDRESS       VARCHAR(512) COMMENT '地址',
  STATUS        TINYINT DEFAULT 1 COMMENT '状态：1-正常，0-停用',
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED    TINYINT(1) DEFAULT 0,
  KEY idx_region (REGION_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='经销商/股东表';

-- T_STORE: 门店表
DROP TABLE IF EXISTS T_STORE;
CREATE TABLE T_STORE (
  STORE_ID      VARCHAR(32) PRIMARY KEY COMMENT '门店ID',
  DISTRIBUTOR_ID VARCHAR(32) COMMENT '所属经销商',
  REGION_ID     VARCHAR(32) COMMENT '所属区域',
  NAME          VARCHAR(255) NOT NULL COMMENT '门店名称',
  ADDRESS       VARCHAR(512) COMMENT '门店地址',
  PHONE         VARCHAR(32) COMMENT '门店电话',
  STATUS        TINYINT DEFAULT 1 COMMENT '状态：1-正常，0-停用',
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED    TINYINT(1) DEFAULT 0,
  KEY idx_distributor (DISTRIBUTOR_ID),
  KEY idx_region (REGION_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='门店表';

-- T_STAFF: 员工表
DROP TABLE IF EXISTS T_STAFF;
CREATE TABLE T_STAFF (
  STAFF_ID      BIGINT(20) AUTO_INCREMENT PRIMARY KEY COMMENT '员工ID',
  DISTRIBUTOR_ID VARCHAR(32) NOT NULL COMMENT '所属经销商',
  STORE_ID      VARCHAR(32) COMMENT '所属门店',
  REGION_ID     VARCHAR(32) COMMENT '所属区域',
  NAME          VARCHAR(64) NOT NULL COMMENT '员工姓名',
  PHONE         VARCHAR(32) NOT NULL COMMENT '手机号(登录账号)',
  PASSWORD_HASH VARCHAR(128) COMMENT '密码哈希',
  ROLE_CODE     VARCHAR(32) DEFAULT 'staff' COMMENT '角色代码',
  STATUS        TINYINT DEFAULT 1 COMMENT '状态：1-正常，0-停用',
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED    TINYINT(1) DEFAULT 0,
  UNIQUE KEY uni_phone (PHONE),
  KEY idx_distributor (DISTRIBUTOR_ID),
  KEY idx_store (STORE_ID),
  KEY idx_region (REGION_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='员工表';

-- -------------------------------------------
-- 二、权限管理
-- -------------------------------------------

-- T_MENU: 菜单表
DROP TABLE IF EXISTS T_MENU;
CREATE TABLE T_MENU (
  MENU_ID       VARCHAR(32) PRIMARY KEY COMMENT '菜单ID',
  MENU_CODE    VARCHAR(64) NOT NULL UNIQUE COMMENT '菜单代码',
  NAME         VARCHAR(128) NOT NULL COMMENT '菜单名称',
  PARENT_ID    VARCHAR(32) COMMENT '父级菜单ID',
