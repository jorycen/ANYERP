-- T_ROLE: 角色表
DROP TABLE IF EXISTS T_ROLE;
CREATE TABLE T_ROLE (
  ROLE_ID       VARCHAR(32) PRIMARY KEY COMMENT '角色ID',
  ROLE_CODE    VARCHAR(64) NOT NULL UNIQUE COMMENT '角色代码',
  NAME         VARCHAR(128) NOT NULL COMMENT '角色名称',
  DESCRIPTION  VARCHAR(512) COMMENT '描述',
  IS_SYSTEM    TINYINT(1) DEFAULT 0 COMMENT '是否系统角色',
  STATUS       TINYINT DEFAULT 1,
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='角色表';

-- T_ROLE_MENU: 角色菜单关联表
DROP TABLE IF EXISTS T_ROLE_MENU;
CREATE TABLE T_ROLE_MENU (
  ID          BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  ROLE_ID     VARCHAR(32) NOT NULL,
  MENU_ID     VARCHAR(32) NOT NULL,
  CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY uni_role_menu (ROLE_ID, MENU_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='角色菜单关联表';

-- T_STAFF_ROLE: 员工角色关联表
DROP TABLE IF EXISTS T_STAFF_ROLE;
CREATE TABLE T_STAFF_ROLE (
  ID          BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  STAFF_ID    VARCHAR(32) NOT NULL,
  ROLE_ID     VARCHAR(32) NOT NULL,
  CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY uni_staff_role (STAFF_ID, ROLE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='员工角色关联表';

-- T_REGION_PERMISSION: 区域数据权限表
DROP TABLE IF EXISTS T_REGION_PERMISSION;
CREATE TABLE T_REGION_PERMISSION (
  ID           BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  STAFF_ID     VARCHAR(32) NOT NULL COMMENT '员工ID',
  REGION_CODE  VARCHAR(32) NOT NULL COMMENT '区域代码',
  CAN_VIEW     TINYINT(1) DEFAULT 1 COMMENT '可查看',
  CAN_MANAGE   TINYINT(1) DEFAULT 0 COMMENT '可管理',
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  KEY idx_staff (STAFF_ID),
  KEY idx_region (REGION_CODE)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='区域数据权限表';

-- -------------------------------------------
-- 三、商品模块
-- -------------------------------------------

-- T_PRODUCT: 商品主表
DROP TABLE IF EXISTS T_PRODUCT;
CREATE TABLE T_PRODUCT (
  PRODUCT_ID      VARCHAR(32) PRIMARY KEY COMMENT '商品ID',
  PRODUCT_CODE    VARCHAR(32) NOT NULL UNIQUE COMMENT '商品编号(P+日期+序号)',
  NAME            VARCHAR(255) NOT NULL COMMENT '商品名称',
  CATEGORY        VARCHAR(64) COMMENT '产品类别',
  NEED_SN         TINYINT(1) DEFAULT 0 COMMENT '是否需要SN',
  NEED_IMEI       TINYINT(1) DEFAULT 0 COMMENT '是否需要IMEI',
  STANDARD_PRICE  DECIMAL(12,2) COMMENT '标准售价',
  COST_PRICE      DECIMAL(12,2) COMMENT '标准成本',
  UNIT            VARCHAR(16) DEFAULT '台' COMMENT '单位',
  SPECS_JSON      JSON COMMENT '规格参数(颜色/容量/版本等)',
  REMARK          VARCHAR(512) COMMENT '备注',
  STATUS          TINYINT DEFAULT 1 COMMENT '状态：1-启用，0-停用',
  CREATE_TIME     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME     TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED      TINYINT(1) DEFAULT 0,
  KEY idx_product_code (PRODUCT_CODE),
  KEY idx_category (CATEGORY),
  KEY idx_name (NAME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品主表';

-- T_PRODUCT_PN: 商品PN料号表
DROP TABLE IF EXISTS T_PRODUCT_PN;
CREATE TABLE T_PRODUCT_PN (
  PN_ID        VARCHAR(32) PRIMARY KEY COMMENT 'PN ID',
  PRODUCT_ID   VARCHAR(32) NOT NULL COMMENT '关联商品ID',
  PN_CODE      VARCHAR(64) NOT NULL COMMENT 'PN料号',
  BARCODE      VARCHAR(64) COMMENT '条形码(69码)',
  IS_PRIMARY   TINYINT(1) DEFAULT 0 COMMENT '是否主料号',
  STATUS       TINYINT DEFAULT 1,
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED   TINYINT(1) DEFAULT 0,
  UNIQUE KEY uni_pn_code (PN_CODE),
  KEY idx_product_pn (PRODUCT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品PN料号表';

-- T_PRODUCT_SN: 商品序列号表
DROP TABLE IF EXISTS T_PRODUCT_SN;
CREATE TABLE T_PRODUCT_SN (
  SN_ID         VARCHAR(32) PRIMARY KEY COMMENT 'SN ID',
  PRODUCT_ID    VARCHAR(32) NOT NULL COMMENT '关联商品ID',
  PN_ID         VARCHAR(32) COMMENT '关联PN ID',
  SN_CODE       VARCHAR(128) NOT NULL COMMENT '序列号',
  IMEI1         VARCHAR(32) COMMENT 'IMEI1(手机类)',
  IMEI2         VARCHAR(32) COMMENT 'IMEI2(手机类)',
  STATUS        VARCHAR(32) DEFAULT 'in_stock' COMMENT '状态:in_stock-在库,sold-已销售,transferring-调拨中,repair-返修',
  STORE_ID      VARCHAR(32) COMMENT '当前门店',
  LOCATION_ID   VARCHAR(32) COMMENT '当前库位',
  INBOUND_TIME  TIMESTAMP COMMENT '入库时间',
  INBOUND_PRICE DECIMAL(12,2) COMMENT '入库价格',
  BATCH_NO      VARCHAR(64) COMMENT '批次号(FIFO)',
  REMARK        VARCHAR(255) COMMENT '备注',
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED    TINYINT(1) DEFAULT 0,
  UNIQUE KEY uni_sn_code (SN_CODE),
  KEY idx_product_sn (PRODUCT_ID),
  KEY idx_store_status (STORE_ID, STATUS),
  KEY idx_batch (BATCH_NO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品序列号表';

-- -------------------------------------------
-- 四、库房管理
-- -------------------------------------------

-- T_LOCATION: 库位表
DROP TABLE IF EXISTS T_LOCATION;
CREATE TABLE T_LOCATION (
  LOCATION_ID  VARCHAR(32) PRIMARY KEY COMMENT '库位ID',
  STORE_ID     VARCHAR(32) NOT NULL COMMENT '所属门店',
  NAME         VARCHAR(64) NOT NULL COMMENT '库位名称',
  TYPE         VARCHAR(32) DEFAULT 'normal' COMMENT '类型:normal-普通,defective-残次区',
  IS_SELLABLE  TINYINT(1) DEFAULT 1 COMMENT '是否允许销售',
  STATUS       TINYINT DEFAULT 1,
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_location_store (STORE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='库位表';

-- T_INVENTORY_WARNING: 库存预警配置表
DROP TABLE IF EXISTS T_INVENTORY_WARNING;
CREATE TABLE T_INVENTORY_WARNING (
  WARNING_ID   VARCHAR(32) PRIMARY KEY,
  PRODUCT_ID   VARCHAR(32) COMMENT '商品ID(为空按类别)',
  CATEGORY     VARCHAR(64) COMMENT '商品类别',
  MIN_STOCK    INT DEFAULT 0 COMMENT '最低库存预警',
  AGING_DAYS   INT DEFAULT 90 COMMENT '库龄预警天数',
  WARNING_TYPE VARCHAR(32) COMMENT '类型:low_stock-库存不足,aging-库龄超期',
  STATUS       TINYINT DEFAULT 1,
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_product_warning (PRODUCT_ID),
  KEY idx_category_warning (CATEGORY)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='库存预警配置表';

-- -------------------------------------------
-- 五、采购管理
-- -------------------------------------------

-- T_SUPPLIER: 供应商表
DROP TABLE IF EXISTS T_SUPPLIER;
CREATE TABLE T_SUPPLIER (
  SUPPLIER_ID   VARCHAR(32) PRIMARY KEY,
  NAME          VARCHAR(255) NOT NULL COMMENT '供应商名称',
  CONTACT       VARCHAR(64) COMMENT '联系人',
  PHONE         VARCHAR(32) COMMENT '联系电话',
  ADDRESS       VARCHAR(512) COMMENT '地址',
  STATUS        TINYINT DEFAULT 1,
  CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED    TINYINT(1) DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='供应商表';

-- T_PURCHASE_REQUEST: 采购申请表
DROP TABLE IF EXISTS T_PURCHASE_REQUEST;
CREATE TABLE T_PURCHASE_REQUEST (
  REQUEST_ID    VARCHAR(32) PRIMARY KEY COMMENT '申请单ID',
  REQUEST_NO   VARCHAR(64) NOT NULL UNIQUE COMMENT '申请单号',
  STORE_ID     VARCHAR(32) NOT NULL COMMENT '申请门店',
  SUPPLIER_ID  VARCHAR(32) COMMENT '供应商',
  TOTAL_AMOUNT DECIMAL(12,2) COMMENT '申请金额',
  REASON       TEXT COMMENT '申请原因',
  STATUS       VARCHAR(32) DEFAULT 'pending' COMMENT '状态:pending-待审批,approved-已审批,rejected-已拒绝,purchased-已采购',
  APPLY_USER   VARCHAR(64) COMMENT '申请人',
  APPROVE_USER VARCHAR(64) COMMENT '审批人',
  APPROVE_COMMENT VARCHAR(512) COMMENT '审批意见',
  CREATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME  TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_request_store (STORE_ID),
  KEY idx_request_status (STATUS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='采购申请表';

-- T_PURCHASE_REQUEST_ITEM: 采购申请明细
DROP TABLE IF EXISTS T_PURCHASE_REQUEST_ITEM;
CREATE TABLE T_PURCHASE_REQUEST_ITEM (
  ITEM_ID      BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  REQUEST_ID   VARCHAR(32) NOT NULL COMMENT '关联申请单',
  PRODUCT_ID   VARCHAR(32) NOT NULL COMMENT '商品ID',
  PRODUCT_NAME VARCHAR(255) COMMENT '商品名称(快照)',
  PN_CODE     VARCHAR(64) COMMENT 'PN料号(快照)',
  QUANTITY    INT NOT NULL COMMENT '申请数量',
  UNIT_PRICE  DECIMAL(12,2) COMMENT '期望单价',
  SUBTOTAL    DECIMAL(12,2) COMMENT '小计',
  KEY idx_request_item (REQUEST_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='采购申请明细表';

-- T_PURCHASE_ORDER: 采购单主表
DROP TABLE IF EXISTS T_PURCHASE_ORDER;
CREATE TABLE T_PURCHASE_ORDER (
  ORDER_ID     VARCHAR(32) PRIMARY KEY COMMENT '采购单ID',
  ORDER_NO    VARCHAR(64) NOT NULL UNIQUE COMMENT '采购单号',
  REQUEST_ID  VARCHAR(32) COMMENT '关联申请单',
  STORE_ID   VARCHAR(32) NOT NULL COMMENT '收货门店',
  SUPPLIER_ID VARCHAR(32) NOT NULL COMMENT '供应商',
  TOTAL_AMOUNT DECIMAL(12,2) COMMENT '采购总金额',
  TOTAL_QUANTITY INT COMMENT '采购总数量',
  STATUS     VARCHAR(32) DEFAULT 'purchased' COMMENT '状态:purchased-已采购,partial_inbound-部分入库,completed-已完成',
  CREATE_USER VARCHAR(64) COMMENT '制单人',
  CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_purchase_store (STORE_ID),
  KEY idx_purchase_supplier (SUPPLIER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='采购单主表';

-- T_PURCHASE_ORDER_ITEM: 采购单明细
DROP TABLE IF EXISTS T_PURCHASE_ORDER_ITEM;
CREATE TABLE T_PURCHASE_ORDER_ITEM (
  ITEM_ID      BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  ORDER_ID     VARCHAR(32) NOT NULL,
  PRODUCT_ID   VARCHAR(32) NOT NULL,
  PRODUCT_NAME VARCHAR(255) COMMENT '商品名称(快照)',
  PN_CODE     VARCHAR(64) COMMENT 'PN料号',
  QUANTITY    INT NOT NULL COMMENT '采购数量',
  UNIT_PRICE  DECIMAL(12,2) COMMENT '采购单价',
  SUBTOTAL    DECIMAL(12,2) COMMENT '小计',
  KEY idx_order_item (ORDER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='采购单明细表';

-- -------------------------------------------
-- 六、销售管理
-- -------------------------------------------

-- T_ORDER: 销售订单主表
DROP TABLE IF EXISTS T_ORDER;
CREATE TABLE T_ORDER (
  ORDER_ID        VARCHAR(32) PRIMARY KEY COMMENT '订单ID',
  ORDER_NO       VARCHAR(64) NOT NULL UNIQUE COMMENT '订单编号',
  STORE_ID       VARCHAR(32) NOT NULL COMMENT '销售门店',
  CREATE_USER   VARCHAR(64) COMMENT '制单人',
  CUSTOMER_NAME  VARCHAR(64) COMMENT '客户姓名',
  CUSTOMER_PHONE VARCHAR(32) COMMENT '客户电话',
  CUSTOMER_SOURCE VARCHAR(64) COMMENT '客户来源',
  TOTAL_AMOUNT   DECIMAL(12,2) NOT NULL DEFAULT 0 COMMENT '订单总金额',
  DISCOUNT_AMOUNT DECIMAL(12,2) DEFAULT 0 COMMENT '优惠金额',
  NATIONAL_SUBSIDY DECIMAL(12,2) DEFAULT 0 COMMENT '国补',
  EDUCATION_SUBSIDY DECIMAL(12,2) DEFAULT 0 COMMENT '教育补贴',
  ACTUAL_PAYMENT DECIMAL(12,2) NOT NULL DEFAULT 0 COMMENT '实际收款',
  INVOICE_STATUS VARCHAR(32) DEFAULT '不开票' COMMENT '开票状态',
  ORDER_STATUS   VARCHAR(32) DEFAULT 'completed' COMMENT '订单状态',
  SUBSIDY_STATUS VARCHAR(32) COMMENT '补贴状态',
  SUBSIDY_PERSON VARCHAR(64) COMMENT '补贴人姓名',
  SUBSIDY_ID    VARCHAR(32) COMMENT '补贴人ID',
  REMARK         TEXT COMMENT '备注',
  CREATE_TIME    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME    TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  IS_DELETED     TINYINT(1) DEFAULT 0,
  KEY idx_order_store (STORE_ID),
  KEY idx_order_no (ORDER_NO),
  KEY idx_order_time (CREATE_TIME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='销售订单主表';

-- T_ORDER_ITEM: 订单明细表
