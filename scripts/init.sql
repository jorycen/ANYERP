-- -------------------------------------------
-- 七、库房操作
-- -------------------------------------------

-- T_INBOUND: 入库单主表
DROP TABLE IF EXISTS T_INBOUND;
CREATE TABLE T_INBOUND (
  INBOUND_ID   VARCHAR(32) PRIMARY KEY COMMENT '入库单ID',
  INBOUND_NO  VARCHAR(64) NOT NULL UNIQUE COMMENT '入库单号',
  STORE_ID    VARCHAR(32) NOT NULL COMMENT '收货门店',
  SOURCE_TYPE VARCHAR(32) COMMENT '来源类型:purchase-采购入库,return-退货入库,transfer-调拨入库',
  SOURCE_NO   VARCHAR(64) COMMENT '来源单号',
  TOTAL_AMOUNT DECIMAL(12,2) COMMENT '入库总金额',
  TOTAL_QUANTITY INT COMMENT '入库总数量',
  STATUS      VARCHAR(32) DEFAULT 'pending' COMMENT '状态:pending-待入库,partial-部分入库,completed-已完成',
  CREATE_USER VARCHAR(64) COMMENT '制单人',
  CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_inbound_store (STORE_ID),
  KEY idx_inbound_no (INBOUND_NO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='入库单主表';

-- T_INBOUND_ITEM: 入库明细表
DROP TABLE IF EXISTS T_INBOUND_ITEM;
CREATE TABLE T_INBOUND_ITEM (
  ITEM_ID     BIGINT(20) AUTO_INCREMENT PRIMARY KEY,
  INBOUND_ID  VARCHAR(32) NOT NULL,
  PRODUCT_ID  VARCHAR(32) NOT NULL,
